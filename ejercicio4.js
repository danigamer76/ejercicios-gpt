// Usa un bucle for para imprimir los números del 1 al 10.
console.log("Usa un bucle for para imprimir los números del 1 al 10")
for (let i = 1; i <= 10; i++) {
    console.log(i);
}

// Usa un bucle while para mostrar los números del 10 al 1.
console.log("Usa un bucle while para mostrar los números del 10 al 1")
let x = 10;
while (x >= 1) {
    console.log(x);
    x--;
}

// Usa un bucle do...while para mostrar los números del 1 al 5.
console.log("Usa un bucle do...while para mostrar los números del 1 al 5")
let y = 1;
do {
    console.log(y);
    y++;
} while (y <= 5);

